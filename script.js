function abrirModal(index) {
  const produto = produtos[index];

  document.getElementById("modal-nome").textContent = produto.nome;
  document.getElementById("modal-preco").textContent = produto.preco;
  document.getElementById("modal-medidas").textContent = produto.medidas;

  const container = document.getElementById("modal-imagens");
  container.innerHTML = "";

  produto.imagens.forEach((src) => {
    const img = document.createElement("img");
    img.src = src;
    img.className = "h-full w-4/5 object-cover rounded-xl cursor-pointer hover:scale-105 transition flex-shrink-0";
    img.onclick = () => abrirImagemExpandida(src);
    container.appendChild(img);
  });

  const mensagem = `Olá, estava dando uma olhada no site e queria saber mais informações sobre o produto ${produto.nome} que custa ${produto.preco}.`;
  const linkWhatsApp = `https://api.whatsapp.com/send/?phone=5585986500500&text=${encodeURIComponent(mensagem)}&type=phone_number&app_absent=0`;
  document.getElementById("modal-botao").href = linkWhatsApp;


  const modal = document.getElementById("modal");
  const modalContent = document.getElementById("modal-content");

  modal.classList.remove("hidden");
  modalContent.classList.add("modal-enter");

  void modalContent.offsetWidth;

  modalContent.classList.add("modal-enter-active");

  setTimeout(() => {
    modalContent.classList.remove("modal-enter", "modal-enter-active");
  }, 300);
}

function abrirImagemExpandida(src) {
  document.getElementById("zoom-img").src = src;
  document.getElementById("zoom-modal").classList.remove("hidden");
}

function fecharZoom() {
  document.getElementById("zoom-modal").classList.add("hidden");
}

function fecharModal() {
  document.getElementById("modal").classList.add("hidden");
}
